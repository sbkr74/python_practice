# find maximum between 2 values
num1 = int(input("Enter your 1st number: "))
num2 = int(input("Enter your 2nd number: "))

if num1>num2:
    print(f"1st number:{num1} is greater than 2nd number:{num2}.")
elif num1<num2:
    print(f"2nd number:{num2} is greater than 1st number:{num1}.")
else:
    print(f"both are equal. {num1} == {num2}")

