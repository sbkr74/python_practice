# User Scan Example
s = input("Enter a String: ")
i = int(input("Enter a decimal value: "))
f = float(input("Enter a float value: "))

add = i+f
sub = i-f
mult = i*f
fdiv = i/f
idiv = int(i//f)

print("String:",s)
print("Integer:",i)
print("Float:",f)
print("Add:",add)
print("Sub:",sub)
print("Mult:",mult)
print("Float div:",fdiv)
print("Int div:",idiv)