# Bitwise Operators
a = 3
b = 2 
c = 16

print("bin>>",bin(a))
print("octal>>",oct(a))
print("hexadecimal>>",hex(c))
print("decimal>>",int(0b10))
print(a>>3)
print(a<<3)
print(a & b)
print(a | b)
print(a ^ b)
print(~a)

